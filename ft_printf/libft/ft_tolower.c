#include "libft.h"

int	ft_tolower(int c)
{
	if (c < -127)
		return (c);
	if (c >= -127 && c < -1)
		return (c + 256);
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	else
		return (c);
}
