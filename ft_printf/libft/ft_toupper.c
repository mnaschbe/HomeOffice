#include "libft.h"

int	ft_toupper(int c)
{
	if (c < -127 + 1)
		return (c);
	if (c >= -127 && c < -1)
		return (c + 256);
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	else
		return (c);
}
